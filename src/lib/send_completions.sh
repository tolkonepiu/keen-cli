## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# keencli completion                                       -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/dannyben/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_keencli_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    echo "$words"'
  echo $'  '
  echo $'  else'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" != "-" ]] && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $''
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_keencli_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=("${COMP_WORDS[@]:1:$COMP_CWORD-1}")'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  case "$compline" in'
  echo $'    \'completions\'*\'--scheme\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "http https")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'completions\'*\'--format\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "json yaml xml props")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'completions\'*\'-f\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "json yaml xml props")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'login\'*\'--scheme\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "http https")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'completions\'*\'-s\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "http https")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "--format --help --host --output --port --scheme --verbose -f -h -o -p -s")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'login\'*\'-s\')'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "http https")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'request\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "--help -h")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    \'login\'*)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "--help --host --port --scheme -h -p -s")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=( "$REPLY" ); done < <( compgen -W "$(_keencli_completions_filter "--help --version -h -v completions login request")" -- "$cur" )'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'complete -F _keencli_completions keencli'
  echo $''
  echo $'# ex: filetype=sh'
}
